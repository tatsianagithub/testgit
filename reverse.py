liste_etu = ["Botros", "Gabriel", "Marc Ricardo", "Jordan", "Alex", "Vincent", "Jordan"]

# Que fait la méthode reverse() sur un liste ?
# Rep: Elle inverse l'ordre des éléments dans la liste.

print("La liste avant d'appliquer la méthode reverse(): ")
print(liste_etu)

liste_etu.reverse()

print("\nLa liste après avoir appliqué la méthode reverse(): ")
print(liste_etu)
